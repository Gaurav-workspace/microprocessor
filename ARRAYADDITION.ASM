.MODEL SMALL
.386
.DATA
    ARRAY1 DB 1H,2H,1H,2H,1H,2H,1H
    ARRAY2 DB 1H,2H,1H,2H,1H,2H,1H
    LEN    DB 7H

    MESSAGE1 DB 'DISPLAYING YOUR ARRAY..$'
.CODE
.STARTUP
    MOV SI,OFFSET ARRAY1
    MOV DI,OFFSET ARRAY2

    MOV CL,LEN

    JMP ELEMENT_SUM

ELEMENT_SUM:
    MOV BL,[SI]
    MOV AL,[DI]

    ADD AL,BL

    MOV [DI],AL

    INC SI
    INC DI

    LOOP ELEMENT_SUM

    JMP DISPLAY_ARRAY
DISPLAY_ARRAY:
    MOV DX,OFFSET MESSAGE1
    MOV AH,09H
    INT 21H

    MOV DL,10
    ; ADD DL,48
    MOV AH,02H
    INT 21H

    MOV CL,LEN
    MOV SI,OFFSET ARRAY2

    JMP DISPLAY_ELEMENT

DISPLAY_ELEMENT:
    MOV DL,[SI]
    ADD DL,48
    MOV AH,02H
    INT 21H

    MOV DL,32
    ; ADD DL,48
    MOV AH,02H
    INT 21H

    INC SI
    
    LOOP DISPLAY_ELEMENT

.EXIT
END